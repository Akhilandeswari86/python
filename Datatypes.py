#Explain about Datatypes


#It represents the kind of value that tells what operations can be performed on a particular data.
#Datatypes are two types: 1.Basic Datatypes
#                          2. Advanced Datatypes 


#Basic Datatypes: 

# Integer : This value is represented by int class. It contains positive or negative whole numbers (without fractions or decimals).
      
# Float : This value is represented by the float class. It is a real number with a floating-point representation. It is specified by a decimal point.

# Complex : A complex number is represented by a complex class. It is specified as (real part) + (imaginary part)j . For example â€“ 2+3j.

# Boolean : Python Data type with one of the two built-in values, True or False. Boolean objects that are equal to True are truthy (true), and those equal to False are falsy (false).

# Char : Character data type allows its variable to store only a single character.



# Advanced Datatypes :

# List : List is an ordered collection of similar or different types of items separated by commas and enclosed within brackets [ ].

# String : String is a sequence of characters represented by either single or double quotes. 

# Tuple : Tuple is an ordered sequence of items same as a list. The only difference is that tuples are immutable. Tuples once created cannot be modified.

# Dictionary : Python dictionary is an ordered collection of items. It stores elements in key/value pairs. Here, keys are unique identifiers that are associated with each value.

# Set : Set is an unordered collection of unique items. Set is defined by values separated by commas inside braces { }.



